public class IncrementoDecremento {
    public static void main(String[] args) {
        int x = 5;
        int y;
        
        // Pré-incremento
        y = ++x;  // x agora é 6, e y também recebe 6
        
        // Pós-incremento
        y = x++;  // y recebe 6, e depois x é incrementado para 7
         
    }
}
