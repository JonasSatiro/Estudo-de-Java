public class Aula006 {
    //A instrução while em Java é uma estrutura de controle de fluxo usada para repetir um bloco de código enquanto uma determinada condição for verdadeira. 
    //Ela verifica a condição antes de executar o bloco de código, por isso é chamada de "laço pré-teste".x

    public static void main(String[] args) {
        
        int numero = 1;

        while (numero <= 5) {
            System.out.println(numero);
            numero++;
        }

    }
}