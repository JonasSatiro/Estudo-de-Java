public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}

// public class Main: Define uma classe chamada Main. Em Java, todo código deve estar dentro de uma classe.

// public static void main(String[] args): Esse é o ponto de entrada do programa. Quando você executa o programa Java, o método main é chamado.

// System.out.println("Hello, World!");: Este comando imprime a frase "Hello, World!" no console loosely.